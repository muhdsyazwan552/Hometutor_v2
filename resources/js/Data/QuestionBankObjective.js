// Enhanced helper functions
export const getQuestionsBySubjectFormTopic = (subject, form, topic, count = 5) => {
  try {
    if (!questionBank[subject] || !questionBank[subject][form] || !questionBank[subject][form][topic]) {
      console.warn(`No questions found for ${subject} - ${form} - ${topic}`);
      return [];
    }

    const questions = questionBank[subject][form][topic];
    
    // Always return exactly 5 questions, shuffle them
    const shuffled = shuffleArray([...questions]);
    return shuffled.slice(0, 5);
  } catch (error) {
    console.error('Error getting questions:', error);
    return [];
  }
};

// Get mixed questions from all topics in a subject-form
export const getMixedQuestionsBySubjectForm = (subject, form, count = 5) => {
  try {
    if (!questionBank[subject] || !questionBank[subject][form]) {
      console.warn(`No questions found for ${subject} - ${form}`);
      return [];
    }

    // Get all questions from all topics in this subject-form
    const allQuestions = [];
    Object.keys(questionBank[subject][form]).forEach(topic => {
      allQuestions.push(...questionBank[subject][form][topic]);
    });

    const shuffled = shuffleArray([...allQuestions]);
    return shuffled.slice(0, count);
  } catch (error) {
    console.error('Error getting mixed questions:', error);
    return [];
  }
};

