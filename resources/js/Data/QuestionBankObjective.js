export const getAllQuestionsBySubjectForm = (subject, form) => {
  try {
    if (!questionBank[subject] || !questionBank[subject][form]) {
      return [];
    }

    const allQuestions = [];
    Object.keys(questionBank[subject][form]).forEach(topic => {
      questionBank[subject][form][topic].forEach(question => {
        allQuestions.push({
          ...question,
          topic: topic // Add topic information to each question
        });
      });
    });

    return allQuestions;
  } catch (error) {
    console.error('Error getting all questions:', error);
    return [];
  }
};

const shuffleArray = (array) => {
  const newArray = [...array];
  for (let i = newArray.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
  }
  return newArray;
};

