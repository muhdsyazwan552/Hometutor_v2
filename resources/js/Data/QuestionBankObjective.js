// Get question count for a specific topic
export const getQuestionCount = (subject, form, topic) => {
  try {
    if (!questionBank[subject] || !questionBank[subject][form] || !questionBank[subject][form][topic]) {
      return 0;
    }
    return questionBank[subject][form][topic].length;
  } catch (error) {
    console.error('Error getting question count:', error);
    return 0;
  }
};

const shuffleArray = (array) => {
  const newArray = [...array];
  for (let i = newArray.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [newArray[i], newArray[j]] = [newArray[j], newArray[i]];
  }
  return newArray;
};

